- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:@"cell"];
    cell.imageView.image = [UIImage imageNamed: @"Group"];
    cell.textLabel.text = [NSString stringWithFormat: @"这是第%d行", (int)indexPath.row];
    NSData *testData = [NSData dataWithContentsOfURL: [NSURL URLWithString: @"https://www.jianshu.com/p/930d7f77df6c"]];
    testData = nil;
    cell.imageView.layer.cornerRadius = 4;
    cell.imageView.layer.shadowOpacity = 0.3;
    cell.imageView.layer.shadowRadius = 1;
    cell.imageView.layer.shadowColor = [UIColor blackColor].CGColor;
    cell.imageView.alpha = 0.3;
    
    return cell;
}

